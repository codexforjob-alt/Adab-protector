last_reply_by_chat: dict[int, str] = {}


def get_reply(
    category: str,
    user_id: int | None = None,
    chat_id: int | None = None,
) -> str:
    del user_id
    replies = REPLIES.get(category) or REPLIES["default"]
    selected = choice(replies)

    if chat_id is not None and len(replies) > 1:
        previous = last_reply_by_chat.get(chat_id)
        if selected == previous:
            alternatives = [reply for reply in replies if reply != previous]
            selected = choice(alternatives)
        last_reply_by_chat[chat_id] = selected

    return selected
